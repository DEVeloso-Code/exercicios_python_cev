# Crie um programa que leia dois números e mostre a soma entre eles.

# Estou solicitando os dois valores, adicionando int para destacar que são números inteiros.
numero1 = int(input("Digite o primeiro valor: "))
numero2 = int(input("Digite o segundo valor: "))

# Estou criando uma str para dizer o que é soma.
soma = numero1 + numero2

# Irei adicionar um print para aparecer o resultado da str soma no terminal.
print("A soma de {} e {} é igual a {}".format(numero1, numero2, soma))




